#include "libft.h"

int	ft_count_words(char const *s, char c)
{
	int word;
	int	i;

	word = 0;
	i = 0;
	while (s[i])
	{
		while (s[i] && s[i] == c)
			i++;
		if (s[i])
		{
			while (s[i] && s[i] != c)
				i++;
			word++;
		}
	}
	return (word);
}
