#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*new_str;

	i = 0;
	new_str = (char*)malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (new_str == 0)
		return (0);
	while (src[i])
	{
		new_str[i] = src[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
